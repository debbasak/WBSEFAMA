		<p>
			After bifurcation of WBSEB into West Bengal State Electricity Distribution Company Limited (WBSEDCL) and West Bengal State Electricity Transmission Company Limited (WBSETCL) it became West Bengal State Electricity Finance & Accounts Managers’ Association (WBSEF&AMA) on June - 2007. The membership of the Association is open to the working officers of Finance & Accounts wing of both WBSEDCL & WBSETCL. After retirement from service the Finance & Accounts Officers can continue to be associated as Life Members.
		</p>
		<p>
			The Executive Committee, elected from amongst the members of the Association, is the decision making body of the Association. The Committee consists of fifteen (15) members as; President - 1, Vice Presidents - 2, General Secretary - 1, Assistant Secretaries - 2, Treasurer - 1, Office Secretary - 1, General Members - 7. The Executive Committee has the tenure of two (2) years.
		</p>
		<p>
			In the direction towards decentralisation of the activities of the Association five Zonal Committees have been constituted in Kolkata, Berhampur, Burdwan, Midnapore and Siliguri. Zonal Committees are constituated from amongst the members posted in the Zones concerned. Matters concerning the members posted at the Zones are to be deliberated upon by the concerned Zonal Committee and then highlighted to the Executive Committee if felt necessary.
		</p>
